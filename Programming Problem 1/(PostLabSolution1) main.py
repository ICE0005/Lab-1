    "def median(numbers):\n",
    "    sorted_numbers = sorted(numbers)\n",
    "    length = len(sorted_numbers)\n",
    "    \n",
    "    if length % 2 == 0:\n",
    "        mid1 = sorted_numbers[length // 2 - 1]\n",
    "        mid2 = sorted_numbers[length // 2]\n",
    "        return (mid1 + mid2) / 2\n",
